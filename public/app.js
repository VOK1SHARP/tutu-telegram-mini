function openTelegramLink(url) {
    console.log('–û—Ç–∫—Ä—ã–≤–∞–µ–º —Å—Å—ã–ª–∫—É:', url);
    
    // –ò–°–ü–†–ê–í–õ–ï–ù–ò–ï: –í–°–ï –°–°–´–õ–ö–ò –ò–î–£–¢ –ù–ê @tea_tea_info
    if (url.includes('@ivan_likhov')) {
        url = url.replace('@ivan_likhov', '@tea_tea_info');
    }
    
    if (url.includes('ivan_likhov')) {
        url = url.replace('ivan_likhov', 'tea_tea_info');
    }
    
    if (window.Telegram && Telegram.WebApp && Telegram.WebApp.openLink) {
        try {
            Telegram.WebApp.openLink(url);
        } catch (error) {
            console.error('Telegram WebApp.openLink failed:', error);
            window.location.href = url;
        }
    } else {
        window.open(url, '_blank', 'noopener,noreferrer');
    }
}

