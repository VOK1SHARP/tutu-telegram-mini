function openTelegramLink(url) {
    console.log('–û—Ç–∫—Ä—ã–≤–∞–µ–º —Å—Å—ã–ª–∫—É –Ω–∞ —É—Å—Ç—Ä–æ–π—Å—Ç–≤–µ:', isIOSDevice() ? 'iOS' : '–î—Ä—É–≥–æ–µ');
    
    // iOS —Ñ–∏–∫—Å: –ø—Ä–æ–≤–µ—Ä—è–µ–º –¥–ª–∏–Ω—É URL
    if (url.length > 2000 && isIOSDevice()) {
        console.warn('‚ö†Ô∏è –°—Å—ã–ª–∫–∞ —Å–ª–∏—à–∫–æ–º –¥–ª–∏–Ω–Ω–∞—è –¥–ª—è iOS, —É–∫–æ—Ä–∞—á–∏–≤–∞–µ–º...');
        // –ê–ª—å—Ç–µ—Ä–Ω–∞—Ç–∏–≤–∞: –æ—Ç–ø—Ä–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ –Ω–æ–º–µ—Ä –∑–∞–∫–∞–∑–∞
        const orderMatch = url.match(/–∑–∞–∫–∞–∑ #(\w+)/i);
        if (orderMatch) {
            const shortMessage = `–ó–∞–∫–∞–∑ ${orderMatch[1]} –≥–æ—Ç–æ–≤ –∫ –æ–±—Ä–∞–±–æ—Ç–∫–µ. –î–µ—Ç–∞–ª–∏ –≤ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏–∏.`;
            url = `https://t.me/ivan_likhov?text=${encodeURIComponent(shortMessage)}`;
        }
    }
    
    if (window.Telegram && Telegram.WebApp && Telegram.WebApp.openLink) {
        try {
            Telegram.WebApp.openLink(url);
        } catch (error) {
            console.error('Telegram WebApp.openLink failed:', error);
            // Fallback –¥–ª—è iOS
            window.location.href = url;
        }
    } else if (isIOSDevice()) {
        // iOS —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–π –º–µ—Ç–æ–¥
        window.location.href = url;
    } else {
        window.open(url, '_blank', 'noopener,noreferrer');
    }
}

