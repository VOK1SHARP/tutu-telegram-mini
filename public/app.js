async function clearOldCache() {
    try {
        // –û—á–∏—Å—Ç–∫–∞ Service Worker –∫—ç—à–∞
        if ('serviceWorker' in navigator) {
            const cacheNames = await caches.keys();
            await Promise.all(
                cacheNames.map(cacheName => {
                    // –£–¥–∞–ª—è–µ–º –≤—Å–µ —Å—Ç–∞—Ä—ã–µ –≤–µ—Ä—Å–∏–∏ –∫—ç—à–∞
                    if (cacheName.startsWith('tea-cache-')) {
                        console.log('üóëÔ∏è –£–¥–∞–ª—è–µ–º —Å—Ç–∞—Ä—ã–π –∫—ç—à:', cacheName);
                        return caches.delete(cacheName);
                    }
                })
            );
        }
        
        // –û—á–∏—Å—Ç–∫–∞ —Å—Ç–∞—Ä–æ–≥–æ localStorage
        const keysToRemove = [];
        for (let i = 0; i < localStorage.length; i++) {
            const key = localStorage.key(i);
            if (key.startsWith('tea_') || key.includes('tea')) {
                keysToRemove.push(key);
            }
        }
        
        keysToRemove.forEach(key => {
            console.log('üóëÔ∏è –£–¥–∞–ª—è–µ–º localStorage –∫–ª—é—á:', key);
            localStorage.removeItem(key);
        });
        
        console.log('‚úÖ –°—Ç–∞—Ä—ã–π –∫—ç—à –∏ –¥–∞–Ω–Ω—ã–µ –æ—á–∏—â–µ–Ω—ã');
        return true;
    } catch (error) {
        console.error('‚ùå –û—à–∏–±–∫–∞ –æ—á–∏—Å—Ç–∫–∏ –∫—ç—à–∞:', error);
        return false;
    }
}

